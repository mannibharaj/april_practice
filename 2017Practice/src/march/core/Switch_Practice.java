package march.core;

public class Switch_Practice {

	public static void main(String[] args) {

		int age;
		
		age = 3;
		
		switch (age) {
		
		case 1: System.out.println("Naureen is 1 year old");
		break;
		
		case 2: System.out.println("Naureen is 2 years old");
		break;
		
		case 3: System.out.println("Naureen is 3 years old");
		break;
		
		case 4: System.out.println("Naureen is 4 years old");
		break;
		
		}
		
	}

}
